public class Pessoa   {
	private String nome;
	private Integer dia;
	private String Personal;
	

	
	public String getPersonal() {
		return Personal;
	}
	public void setPersonal(String personal) {
		Personal = personal;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Integer getDia() {
		return dia;
	}
	public void setDia(Integer dia) {
		this.dia = dia;
	}
}

 
